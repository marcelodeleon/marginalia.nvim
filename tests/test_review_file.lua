    MiniTest.expect.equality(entries[1].line, 10)
    MiniTest.expect.equality(entries[1].line_end, 15)
    MiniTest.expect.equality(entries[1].status, "resolved")
end

T['setup']['parse multiple comments with separator'] = function()
    vim.fn.writefile({
        "## a.lua:1",
