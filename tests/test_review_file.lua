-- ── round-trip ───────────────────────────────────────────────────────

T['setup']['round-trip preserves comment data'] = function()
    write_source(tmpdir, "lib.lua", {
        "-- header",
        "local M = {}",
        "function M.go() end",
        "return M",
    })

    local original = {
        { file = "lib.lua", line = 2, body = "Add docstring", status = "open" },
        { file = "lib.lua", line = 3, line_end = 3, body = "Needs error handling\nand logging", status = "resolved" },
    }

    review_file.serialize(original, tmpdir, "REVIEW.md")
    local parsed = review_file.parse(tmpdir .. "/REVIEW.md")

    MiniTest.expect.equality(#parsed, 2)

    MiniTest.expect.equality(parsed[1].file, "lib.lua")
    MiniTest.expect.equality(parsed[1].line, 2)
    MiniTest.expect.equality(parsed[1].body, "Add docstring")
    MiniTest.expect.equality(parsed[1].status, "open")

    MiniTest.expect.equality(parsed[2].file, "lib.lua")
    MiniTest.expect.equality(parsed[2].line, 3)
    MiniTest.expect.equality(parsed[2].line_end, 3)
    MiniTest.expect.equality(parsed[2].body, "Needs error handling\nand logging")
    MiniTest.expect.equality(parsed[2].status, "resolved")
end

T['setup']['round-trip with multiple files'] = function()
    write_source(tmpdir, "a.py", { "def a(): pass" })
    write_source(tmpdir, "z.py", { "def z(): pass" })

    local original = {
        { file = "z.py", line = 1, body = "Z comment", status = "open" },
        { file = "a.py", line = 1, body = "A comment", status = "open" },
    }

    review_file.serialize(original, tmpdir, "REVIEW.md")
    local parsed = review_file.parse(tmpdir .. "/REVIEW.md")

    MiniTest.expect.equality(#parsed, 2)
    -- serialize sorts by file, so a.py comes first
    MiniTest.expect.equality(parsed[1].file, "a.py")
    MiniTest.expect.equality(parsed[2].file, "z.py")
end

return T
